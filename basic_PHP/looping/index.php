<?php

    for($i = 0; $i < 5; $i++){
        echo "$i. ";
        echo 'Aan membeli rokok';
        echo '<br>';
    }

    echo '<br>';

    // While -> cek kondisi -> jalankan tugas
    $i = 0;
    while($i < 5) {
        echo "$i. ";
        echo 'Task';
        echo '<br>';
        $i++;
    }

    echo '<br>';

    // Do While -> jalankan tugas -> cek kondisi
    $i = 0; 
    do {
        echo "$i. ";
        echo 'Tugas';
        echo '<br>';
        $i++;
    } while ($i < 5)
?>

